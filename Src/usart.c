void Zeng_uart_os_tran(UART_HandleTypeDef *huart, uint8_t *data, uint16_t cnt)
{
  osSemaphoreWait(sem_uart_dma, 5);
  HAL_UART_Transmit_DMA(huart, data, cnt);
}


void dma_printf(const char *format,...)
{
	uint32_t length;
	va_list args;
  osSemaphoreWait(sem_uart_dma, 5);
	va_start(args, format);
	length = vsnprintf((char*)txbuf, sizeof(txbuf), (char*)format, args);
	va_end(args);
	HAL_UART_Transmit_DMA(&huart3, (uint8_t*)txbuf, length);
}

